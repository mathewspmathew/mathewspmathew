function redirect(str) {
    switch (str) {
        case 'musk':
            window.location.href = "https://twitter.com/mathews_pmathew";
            break;
        case 'gh':
            window.location.href = "https://github.com/mathewspmathew";
            break;
        case 'link':
            window.location.href = "https://www.linkedin.com/in/mathews-p-mathew-a091281b7";
            break;
        case 'drbl':
            window.location.href = "https://www.behance.net/gallery/142297339/Competition-poster/modules/803894201";
            break;
        default:
            break;
    }
}